char	*ft_strcpy(char *dest, char const *src)
{
	int	i;

	if (dest && src)
	{
		i = 0;
		while (src[i])
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = src[i];
	}
	return (dest);
}
