int		ft_bouton(int i, int j, int k)
{
	int		a;

	if ((i >= j && j >= k) || (i <= j && j <= k))
		a = j;
	if ((i >= k && k >= j) || (i <= k && k <= j))
		a = k;
	if ((i >= j && i <= k) || (i <= j && i >= k))
		a = i;
	return (a);
}
