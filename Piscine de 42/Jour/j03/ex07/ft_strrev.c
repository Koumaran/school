char	*ft_strrev(char *str)
{
	int		i;
	int		a;
	char	c;

	i = 0;
	a = 0;
	while (str[i] > 0)
		i++;
	while (a < i / 2)
	{
		c = *(str + a);
		str[a] = str[i - a - 1];
		*(str + i - a - 1) = c;
		a++;
	}
	return (str);
}
